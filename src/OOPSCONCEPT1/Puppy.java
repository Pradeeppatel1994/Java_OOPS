package OOPSCONCEPT1;

public class Puppy extends Dog{

	@Override
	public void eat() {
		System.out.println("Puppy...eat");
	}
	
	@Override
	public void bark() {
		System.out.println("Puppy...bark");
	}
	
	public void drink() {
		System.out.println("Puppy...drink");
	}
	public void run() {
		System.out.println("Puppy...run");
	}
	

}
