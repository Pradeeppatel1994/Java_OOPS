package OOPSCONCEPT1;

public class Car {
	
	
	public void start() {
		System.out.println("Car...Start");
	}

	public void run() {
		System.out.println("Car...Run");
	}
	public void powerBreak() {
		System.out.println("Car....PowerBreak");
	}
	
	public void engine() {
		System.out.println("Car....engine");
	}
	
	
	public void stop() {
		System.out.println("Car....Stop");
	}
	
	
}
